async function runVehicleMonthlyInspection(): Promise<void> {
  const now = new Date();
  const monthStart = new Date(now.getFullYear(), now.getMonth(), 1);

  const existingInspection = await db
    .select()
    .from(notificationsTable)
    .where(
      and(
        eq(notificationsTable.notificationType, "vehicle_monthly_inspection"),
        gte(notificationsTable.createdAt, monthStart)
      )
    );

  if (existingInspection.length > 0) {
    logger.info("Vehicle monthly inspection already run this month, skipping");
    return;
  }

  const allVehicles = await db
    .select()
    .from(vehiclesTable)
    .where(eq(vehiclesTable.status, "registered"));

  const registeredUnits = new Set(allVehicles.map((v) => v.unit));

  const allTenants = await db
    .select({ unit: tenantsTable.unit, tenantName: tenantsTable.tenantName })
    .from(tenantsTable)
    .where(eq(tenantsTable.status, "active"));

  const unregisteredUnits = allTenants.filter((t) => !registeredUnits.has(t.unit));

  if (unregisteredUnits.length > 0) {
    const unitList = unregisteredUnits
      .slice(0, 10)
      .map((u) => `${u.unit}호`)
      .join(", ");
    const suffix = unregisteredUnits.length > 10 ? ` 외 ${unregisteredUnits.length - 10}건` : "";

    await insertNotification({
      recipientType: "admin",
      notificationType: "vehicle_monthly_inspection",
      title: "월별 차량 점검 알림",
      message: `미등록 차량 ${unregisteredUnits.length}건 확인 필요: ${unitList}${suffix}`,
      relatedEntityType: "vehicle",
    });

    logger.info({ count: unregisteredUnits.length }, "Vehicle monthly inspection alert created");
  }
}

async function runWeeklyReportAutoGeneration(): Promise<void> {
  const today = new Date();
  const dayOfWeek = today.getDay();
  if (dayOfWeek !== 1) return;

  const mondayStr = today.toISOString().split("T")[0];
  const lastSunday = new Date(today);
  lastSunday.setDate(today.getDate() - 1);
  const lastSundayStr = lastSunday.toISOString().split("T")[0];
  const lastMonday = new Date(today);
  lastMonday.setDate(today.getDate() - 7);
  const lastMondayStr = lastMonday.toISOString().split("T")[0];

  const existing = await db.select().from(weeklySummaryReportsTable)
    .where(eq(weeklySummaryReportsTable.weekStart, lastMondayStr));
  if (existing.length > 0) {
    logger.info("Weekly report already exists for this week, skipping");
    return;
  }

  const dailyReports = await db.select().from(dailyReportsTable)
    .where(and(
      gte(dailyReportsTable.reportDate, lastMondayStr),
      lte(dailyReportsTable.reportDate, lastSundayStr)
    ));

  const typeLabel: Record<string, string> = {
    expense: "경비", cleaning: "미화", maintenance: "유지보수",
    security: "보안", other: "기타",
  };
  const typeCount: Record<string, number> = {};
  for (const dr of dailyReports) {
    const label = typeLabel[dr.reportType] || dr.reportType;
    typeCount[label] = (typeCount[label] || 0) + 1;
  }

  const allInspections = await db.select().from(inspectionsTable);
  const weekInspDue = allInspections.filter(
    (i) => i.nextDueDate >= lastMondayStr && i.nextDueDate <= lastSundayStr
  );
  const allLogs = await db.select().from(inspectionLogsTable);
  const weekLogs = allLogs.filter(
    (l) => l.inspectionDate >= lastMondayStr && l.inspectionDate <= lastSundayStr
  );

  let inspSection = "";
  if (weekInspDue.length > 0 || weekLogs.length > 0) {
    inspSection = "\n\n■ 금주 점검 현황";
    if (weekLogs.length > 0) {
      inspSection += `\n  완료된 점검: ${weekLogs.length}건`;
    }
    const pending = weekInspDue.filter(
      (i) => !weekLogs.some((l) => l.inspectionId === i.id)
    );
    if (pending.length > 0) {
      inspSection += `\n  미완료 점검: ${pending.length}건`;
    }
  }

  const summary = `[자동 생성] 주간 보고 요약 (${lastMondayStr} ~ ${lastSundayStr})\n\n총 ${dailyReports.length}건의 일간 보고서\n${Object.entries(typeCount).map(([t, c]) => `${t}: ${c}건`).join("\n")}\n\n${dailyReports.map((dr) => `- [${dr.reportDate}] ${dr.title}`).join("\n")}${inspSection}`;

  await db.insert(weeklySummaryReportsTable).values({
    title: `[자동] ${lastMondayStr} 주간 보고`,
    weekStart: lastMondayStr,
    weekEnd: lastSundayStr,
    summary,
    totalDailyReports: dailyReports.length,
    dailyReportIds: dailyReports.map((d) => d.id).join(","),
    authorId: 0,
    authorName: "시스템",
    status: "draft",
  });

  logger.info({ weekStart: lastMondayStr, dailyCount: dailyReports.length }, "Auto-generated weekly report");
}

async function computeBuildingAccountingData(buildingId: number, reportMonth: string) {
  const buildingUnits = await db.select().from(unitsTable)
    .where(eq(unitsTable.buildingId, buildingId));
  const unitIds = new Set(buildingUnits.map(u => u.id));
  const unitCount = buildingUnits.length;

  const allBilling = await db.select().from(monthlyPaymentsTable)
    .where(eq(monthlyPaymentsTable.billingMonth, reportMonth));
  const billingRecords = allBilling.filter(r => unitIds.has(r.unitId));

  let totalBilled = 0, totalCollected = 0, unpaidCount = 0, unpaidAmount = 0;
  if (billingRecords.length > 0) {
    totalBilled = billingRecords.reduce((s, r) => s + r.totalAmount, 0);
    totalCollected = billingRecords.reduce((s, r) => s + r.paidAmount, 0);
    unpaidCount = billingRecords.filter(r => !r.isPaid).length;
    unpaidAmount = Math.round(totalBilled - totalCollected);
  }
  const collectionRate = totalBilled > 0 ? Math.round((totalCollected / totalBilled) * 1000) / 10 : 0;

  const prevMonthDate = new Date(parseInt(reportMonth.split("-")[0]), parseInt(reportMonth.split("-")[1]) - 2, 1);
  const prevMonth = `${prevMonthDate.getFullYear()}-${String(prevMonthDate.getMonth() + 1).padStart(2, "0")}`;
  const allPrevBilling = await db.select().from(monthlyPaymentsTable)
    .where(eq(monthlyPaymentsTable.billingMonth, prevMonth));
  const prevBillingRecords = allPrevBilling.filter(r => unitIds.has(r.unitId));

  let momChangePct: number | null = null;
  if (prevBillingRecords.length > 0) {
    const prevTotal = prevBillingRecords.reduce((s, r) => s + r.totalAmount, 0);
    if (prevTotal > 0) {
      momChangePct = Math.round(((totalBilled - prevTotal) / prevTotal) * 1000) / 10;
    }
  }

  const allTenants = await db.select().from(tenantsTable);
  const occupantCardCount = allTenants.filter(t => t.unitId && unitIds.has(t.unitId) && t.status === "active").length;

  const allVehicles = await db.select().from(vehiclesTable);
  const vehicleCardCount = allVehicles.filter(v => v.buildingId === buildingId && v.status === "registered").length;

  return { totalBilled, totalCollected, collectionRate, unpaidAmount, unpaidCount, momChangePct, occupantCardCount, vehicleCardCount, unitCount };
}

async function runMonthlyReportAutoGeneration(): Promise<void> {
  const today = new Date();
  const dayOfWeek = today.getDay();
  if (dayOfWeek !== 1 || today.getDate() > 7) return;

  const lastMonth = new Date(today.getFullYear(), today.getMonth() - 1, 1);
  const reportMonth = `${lastMonth.getFullYear()}-${String(lastMonth.getMonth() + 1).padStart(2, "0")}`;

  const existing = await db.select().from(monthlySummaryReportsTable)
    .where(eq(monthlySummaryReportsTable.reportMonth, reportMonth));

  const weeklyReports = await db.select().from(weeklySummaryReportsTable)
    .where(sql`${weeklySummaryReportsTable.weekStart} LIKE ${reportMonth + "%"}`);

  const totalDailyCount = weeklyReports.reduce((s, r) => s + r.totalDailyReports, 0);

  const monthStart = `${reportMonth}-01`;
  const monthEndDate = new Date(parseInt(reportMonth.split("-")[0]), parseInt(reportMonth.split("-")[1]), 0);
  const monthEnd = monthEndDate.toISOString().split("T")[0];

  const allLogs = await db.select().from(inspectionLogsTable);
  const monthLogs = allLogs.filter(
    (l) => l.inspectionDate >= monthStart && l.inspectionDate <= monthEnd
  );

  let inspSummary = "";
  if (monthLogs.length > 0) {
    const resultCounts: Record<string, number> = {};
    for (const l of monthLogs) {
      resultCounts[l.result] = (resultCounts[l.result] || 0) + 1;
    }
    inspSummary = `\n\n■ 월간 점검 현황\n  완료: ${monthLogs.length}건\n  결과: ${Object.entries(resultCounts).map(([r, c]) => `${r} ${c}건`).join(", ")}`;
  }

  const buildings = await db.select().from(buildingsTable);

  for (const building of buildings) {
    const existingForBuilding = existing.find(e => e.buildingId === building.id);
    if (existingForBuilding) continue;

    const acct = await computeBuildingAccountingData(building.id, reportMonth);

    let accountingSummary = "";
    if (acct.totalBilled > 0) {
      const paidCount = acct.unitCount - acct.unpaidCount;
      let momChange = "";
      if (acct.momChangePct !== null) {
        const arrow = acct.momChangePct > 0 ? "▲" : acct.momChangePct < 0 ? "▼" : "→";
        momChange = `\n  전월 대비: ${arrow} ${acct.momChangePct > 0 ? "+" : ""}${acct.momChangePct}%`;
      }
      accountingSummary = `\n\n■ 회계 현황\n  부과 총액: ₩${Math.round(acct.totalBilled).toLocaleString()}\n  수납 총액: ₩${Math.round(acct.totalCollected).toLocaleString()}\n  수납률: ${acct.collectionRate}% (${paidCount}/${acct.unitCount}세대)\n  미납 세대: ${acct.unpaidCount}세대 (₩${acct.unpaidAmount.toLocaleString()})${momChange}`;
    }

    const summary = `[자동 생성] 월간 보고 요약 (${reportMonth}) - ${building.name}\n\n총 ${weeklyReports.length}건의 주간 보고서\n총 ${totalDailyCount}건의 일간 보고서 집계\n\n${weeklyReports.map((wr) => `- ${wr.title} (일간 보고 ${wr.totalDailyReports}건)`).join("\n")}${inspSummary}${accountingSummary}\n\n■ 현황 지표\n  입주자카드 작성: ${acct.occupantCardCount}/${acct.unitCount}세대\n  차량 등록: ${acct.vehicleCardCount}대`;

    await db.insert(monthlySummaryReportsTable).values({
      title: `[자동] ${reportMonth} 월간 보고 - ${building.name}`,
      reportMonth,
      buildingId: building.id,
      summary,
      totalWeeklyReports: weeklyReports.length,
      weeklyReportIds: weeklyReports.map((w) => w.id).join(","),
      totalBilled: acct.totalBilled,
      totalCollected: acct.totalCollected,
      collectionRate: acct.collectionRate,
      unpaidAmount: acct.unpaidAmount,
      unpaidUnits: acct.unpaidCount,
      occupantCardCount: acct.occupantCardCount,
      totalUnits: acct.unitCount,
      vehicleCardCount: acct.vehicleCardCount,
      momChangePct: acct.momChangePct,
      authorId: 0,
      authorName: "시스템",
      status: "draft",
    });

    logger.info({ reportMonth, buildingId: building.id, buildingName: building.name }, "Auto-generated monthly report for building");
  }
}

async function computeUnitBillingStatus(unitId: number): Promise<{ overdueMonths: number; totalOverdueAmount: number } | null> {
  const now = new Date();
  const cutoffDate = `${now.getFullYear()}-${String(now.getMonth() + 1).padStart(2, "0")}-25`;

  const unpaidRecords = await db.select().from(monthlyPaymentsTable)
    .where(
      and(
        eq(monthlyPaymentsTable.unitId, unitId),
        eq(monthlyPaymentsTable.isPaid, false),
        lte(monthlyPaymentsTable.dueDate, cutoffDate)
      )
    );

  if (unpaidRecords.length < 2) return null;

  const totalOverdueAmount = unpaidRecords.reduce((sum, r) => sum + (r.totalAmount - r.paidAmount), 0);

  return {
    overdueMonths: unpaidRecords.length,
    totalOverdueAmount: Math.round(totalOverdueAmount),
  };
}

async function runDelinquencyDetection(): Promise<void> {
  const todayStr = getToday();

  const existingCheck = await db
    .select()
    .from(notificationsTable)
    .where(
      and(
        eq(notificationsTable.notificationType, "delinquency_detection"),
        gte(notificationsTable.createdAt, new Date(todayStr))
      )
    );

  if (existingCheck.length > 0) {
    logger.info("Delinquency detection already run today, skipping");
    return;
  }

  const allUnits = await db.select().from(unitsTable)
    .where(eq(unitsTable.status, "occupied"));

  if (allUnits.length === 0) return;

  const allTenants = await db.select().from(tenantsTable)
    .where(eq(tenantsTable.status, "active"));

  const tenantByUnit = new Map<number, typeof allTenants[0]>();
  for (const t of allTenants) {
    if (t.unitId) tenantByUnit.set(t.unitId, t);
  }

  const existingActive = await db.select().from(delinquencyActionsTable)
    .where(eq(delinquencyActionsTable.status, "active"));
  const activeUnitIds = new Set(existingActive.map(a => a.unitId));

  let detectedCount = 0;

  for (const unit of allUnits) {
    if (activeUnitIds.has(unit.id)) continue;

    const billing = await computeUnitBillingStatus(unit.id);
    if (!billing) continue;

    const tenant = tenantByUnit.get(unit.id);

    await db.insert(delinquencyActionsTable).values({
      unitId: unit.id,
      unitNumber: unit.unitNumber,
      tenantId: tenant?.id ?? null,
      tenantName: tenant?.tenantName ?? null,
      overdueMonths: billing.overdueMonths,
      totalOverdueAmount: billing.totalOverdueAmount,
      actionType: "detected",
      status: "active",
      performedBy: "system",
      notes: `[${todayStr}] 자동 감지: ${billing.overdueMonths}개월 연체, 미납액 ${billing.totalOverdueAmount.toLocaleString()}원`,
    });

    detectedCount++;
  }

  if (detectedCount > 0) {
    await insertNotification({
      recipientType: "admin",
      notificationType: "delinquency_detection",
      title: "연체 세대 자동 감지",
      message: `${detectedCount}건의 2개월 이상 연체 세대가 감지되었습니다. 연체 관리에서 확인하세요.`,
      relatedEntityType: "delinquency",
    });

    logger.info({ count: detectedCount }, "Delinquency detection completed");
  }
}

async function runDelinquencyAutoResolution(): Promise<void> {
  const todayStr = getToday();

  const activeActions = await db.select().from(delinquencyActionsTable)
    .where(eq(delinquencyActionsTable.status, "active"));

  if (activeActions.length === 0) return;

  let resolvedCount = 0;

  for (const action of activeActions) {
    if (!action.unitId) continue;

    const [unit] = await db.select().from(unitsTable)
      .where(eq(unitsTable.id, action.unitId));
    if (!unit) continue;

    const billing = await computeUnitBillingStatus(unit.id);

    if (billing) continue;

    if (action.actionType === "parking_suspended") {
      const suspendedVehicles = await db.select().from(vehiclesTable)
        .where(and(
          eq(vehiclesTable.unit, action.unitNumber),
          eq(vehiclesTable.buildingId, unit.buildingId),
          eq(vehiclesTable.status, "suspended")
        ));

      for (const v of suspendedVehicles) {
        await db.update(vehiclesTable)
          .set({ status: "registered" })
          .where(eq(vehiclesTable.id, v.id));

        await db.insert(vehicleHistoryTable).values({
          vehicleId: v.id,
          action: "reactivated",
          vehicleNumber: v.vehicleNumber,
          unit: v.unit,
          performedBy: "system",
          notes: "연체 해소 자동 감지로 주차권 복원",
        });
      }
    }

    await db.update(delinquencyActionsTable)
      .set({
        status: "resolved",
        resolvedDate: new Date(),
        notes: `${action.notes ? action.notes + "\n" : ""}[${todayStr}] 자동 해소: 미납 해소 감지`,
      })
      .where(eq(delinquencyActionsTable.id, action.id));

    resolvedCount++;
  }

  if (resolvedCount > 0) {
    await insertNotification({
      recipientType: "admin",
      notificationType: "delinquency_auto_resolved",
      title: "연체 자동 해소",
      message: `${resolvedCount}건의 연체가 수납 확인으로 자동 해소되었습니다.`,
      relatedEntityType: "delinquency",
    });

    logger.info({ count: resolvedCount }, "Delinquency auto-resolution completed");
  }
}

// [Task #389] 공고문 게시 제안업무 자동화 — 정기 게시 스케줄이 설정된 공지 템플릿을
//   매니저 별 buildingId 기준으로 평가해 발생일 leadDays 이전부터 매니저 dashboard
//   "제안업무"에 노출되는 알림(notifications) 을 멱등하게 생성한다.
//   - 같은 (template, building, occurrence) 조합 알림이 이미 만들어져 있으면 다시
//     만들지 않는다 (relatedEntityType="building_notice_template",
//     relatedEntityId=templateId 의 message 안에 occurrence 문자열을 포함시켜 식별).
//   - 매니저 대시보드 화면 자체는 이미 dashboard.ts 가 같은 규칙으로 즉시 알림을
//     생성하므로, 이 잡은 알림함(notifications) 행을 채워 추후 푸시/메일 트리거의
//     싱글 소스로 활용하기 위한 백업 기록이다.
async function runNoticePostingSuggestion(): Promise<void> {
  const todayStr = getToday();
  const today = new Date(todayStr);

  const activeTemplates = await db
    .select()
    .from(buildingNoticeTemplatesTable)
    .where(eq(buildingNoticeTemplatesTable.isActive, true));
  const scheduled = activeTemplates.filter(
    (t) => t.scheduleType && t.scheduleType !== "none",
  );
  if (scheduled.length === 0) return;

  // 알림 대상 건물: 활성 매니저(또는 회계/시설)가 소속된 건물만 대상.
  const staff = await db
    .select({ id: usersTable.id, buildingId: usersTable.buildingId })
    .from(usersTable);
  const buildingIds = Array.from(
    new Set(staff.map((s) => s.buildingId).filter((id): id is number => typeof id === "number")),
  );
  if (buildingIds.length === 0) return;
  // userId → buildingId 맵: alertActions 가 building 컬럼을 갖지 않으므로
  // 처리완료된 액션의 owner user 를 통해 건물을 식별한다 (멱등 dedupe 용).
  const userBuildingMap = new Map<number, number | null>();
  for (const u of staff) {
    userBuildingMap.set(u.id, u.buildingId ?? null);
  }

  function pad2(n: number): string {
    return n < 10 ? `0${n}` : `${n}`;
  }
  function ymd(d: Date): string {
    return `${d.getFullYear()}-${pad2(d.getMonth() + 1)}-${pad2(d.getDate())}`;
  }
  function nextYearlyOccurrence(month: number, day: number): string {
    let candidate = new Date(today.getFullYear(), month - 1, day);
    if (ymd(candidate) < todayStr) {
      candidate = new Date(today.getFullYear() + 1, month - 1, day);
    }
    return ymd(candidate);
  }
  function nextMonthlyOccurrence(day: number): string {
    let candidate = new Date(today.getFullYear(), today.getMonth(), day);
    if (ymd(candidate) < todayStr) {
      candidate = new Date(today.getFullYear(), today.getMonth() + 1, day);
    }
    return ymd(candidate);
  }

  let createdCount = 0;
  for (const tpl of scheduled) {
    const cfg = (tpl.scheduleConfig as Record<string, unknown> | null) ?? null;
    for (const buildingId of buildingIds) {
      let occurrence: string | null = null;
      if (tpl.scheduleType === "yearly") {
        const month = Number(cfg?.month);
        const day = Number(cfg?.day);
        if (!Number.isFinite(month) || !Number.isFinite(day)) continue;
        occurrence = nextYearlyOccurrence(month, day);
      } else if (tpl.scheduleType === "monthly") {
        const day = Number(cfg?.day);
        if (!Number.isFinite(day)) continue;
        occurrence = nextMonthlyOccurrence(day);
      } else if (tpl.scheduleType === "before_inspection") {
        const inspectionName = typeof cfg?.inspectionName === "string" ? cfg.inspectionName : null;
        if (!inspectionName) continue;
        const matched = await db
          .select()
          .from(inspectionsTable)
          .where(and(eq(inspectionsTable.buildingId, buildingId), eq(inspectionsTable.name, inspectionName)));
        const upcoming = matched
          .filter((i) => i.nextDueDate >= todayStr)
          .sort((a, b) => (a.nextDueDate < b.nextDueDate ? -1 : 1));
        if (upcoming.length === 0) continue;
        occurrence = upcoming[0].nextDueDate;
      }
      if (!occurrence) continue;

      const leadDays = tpl.leadDays ?? 7;
      const occMs = new Date(occurrence).getTime();
      const todayMs = today.getTime();
      const daysUntil = Math.ceil((occMs - todayMs) / (1000 * 60 * 60 * 24));
      if (daysUntil > leadDays || daysUntil < 0) continue;

      // 멱등 체크 1: 동일 (template, building, occurrence) 알림이 이미 있는지.
      const existing = await db
        .select()
        .from(notificationsTable)
        .where(
          and(
            eq(notificationsTable.notificationType, "notice_posting_suggestion"),
            eq(notificationsTable.relatedEntityType, "building_notice_template"),
            eq(notificationsTable.relatedEntityId, tpl.id),
          ),
        );
      const tag = `B${buildingId}@${occurrence}`;
      if (existing.some((n) => n.message.includes(tag))) continue;

      // 멱등 체크 2: 같은 occurrence 가 이미 처리완료/연기됐다면 (해당 건물의
      // 매니저가 누른 alertActions 가 존재) 알림 행 자체를 만들지 않는다.
      const acted = await db
        .select()
        .from(alertActionsTable)
        .where(
          and(
            eq(alertActionsTable.alertType, "notice_posting"),
            eq(alertActionsTable.relatedEntityType, "building_notice_template"),
            eq(alertActionsTable.relatedEntityId, tpl.id),
          ),
        );
      const suppressed = acted.some((a) => {
        const actionBuilding = a.userId != null ? userBuildingMap.get(a.userId) ?? null : null;
        if (actionBuilding !== buildingId) return false;
        if (a.actedOnDueDate && a.actedOnDueDate >= occurrence!) return true;
        if (a.actionType === "completed" && a.completedDate && a.completedDate >= occurrence!) return true;
        if (a.actionType === "postponed" && a.postponeDays) {
          const actionDate = new Date(a.createdAt);
          const suppressUntil = new Date(actionDate.getTime() + a.postponeDays * 24 * 60 * 60 * 1000);
          if (today < suppressUntil) return true;
        }
        return false;
      });
      if (suppressed) continue;

      await insertNotification({
        recipientType: "admin",
        notificationType: "notice_posting_suggestion",
        title: `${tpl.title} 게시 예정`,
        message: `${tag} ${occurrence}까지 「${tpl.title}」 공지문 게시가 필요합니다 (D-${daysUntil}).`,
        relatedEntityType: "building_notice_template",
        relatedEntityId: tpl.id,
      });
      createdCount++;
    }
  }
  if (createdCount > 0) {
    logger.info({ count: createdCount }, "Notice posting suggestions created");
  }
}

