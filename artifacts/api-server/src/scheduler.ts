//   - 본사 관리자는 platform_settings 의 발송 시각을 바꿀 수 있다 (HH:MM 24h).
//
// 발송 시각 키 / 기본값:
//   daily_journal_reminder_evening_at = "18:00"  (그 시각 이후 첫 tick 에서 평가)
//   daily_journal_reminder_morning_at = "09:30"  (그 시각 이후 첫 tick 에서 평가)

const REMINDER_TARGET_ROLES = ["manager", "accountant", "facility_staff"] as const;
const REMINDER_EVENING_KEY = "daily_journal_reminder_evening_at";
const REMINDER_MORNING_KEY = "daily_journal_reminder_morning_at";
const REMINDER_DEFAULT_EVENING = "18:00";
const REMINDER_DEFAULT_MORNING = "09:30";

function pad2(n: number): string {
  return n < 10 ? `0${n}` : String(n);
}

function kstNow(): Date {
  // UTC + 9h. Date 객체의 UTC getter 들을 써서 "KST 의 시·분·요일·일자" 를 읽는다.
  return new Date(Date.now() + 9 * 60 * 60 * 1000);
}

function kstYmd(d: Date): string {
  return `${d.getUTCFullYear()}-${pad2(d.getUTCMonth() + 1)}-${pad2(d.getUTCDate())}`;
}

function kstAddDays(ymd: string, delta: number): string {
  const [y, m, d] = ymd.split("-").map(Number);
  const t = Date.UTC(y, m - 1, d) + delta * 24 * 60 * 60 * 1000;
  const dt = new Date(t);
  return `${dt.getUTCFullYear()}-${pad2(dt.getUTCMonth() + 1)}-${pad2(dt.getUTCDate())}`;
}

function startOfTodayKstAsUtc(): Date {
  // KST 자정의 UTC 표현 = (KST 현재의 자정) - 9h.
  const k = kstNow();
  const midnightKstAsUtcMs = Date.UTC(
    k.getUTCFullYear(), k.getUTCMonth(), k.getUTCDate(), 0, 0, 0, 0
  ) - 9 * 60 * 60 * 1000;
  return new Date(midnightKstAsUtcMs);
}

function parseHHmm(value: string | null | undefined, fallback: string): { h: number; m: number; raw: string } {
  const raw = (value && /^\d{1,2}:\d{2}$/.test(value)) ? value : fallback;
  const [hh, mm] = raw.split(":").map(Number);
  const h = Math.min(23, Math.max(0, hh));
  const m = Math.min(59, Math.max(0, mm));
  return { h, m, raw: `${pad2(h)}:${pad2(m)}` };
}

async function getReminderSetting(key: string, fallback: string): Promise<{ h: number; m: number; raw: string }> {
  try {
    const [row] = await db
      .select({ value: platformSettingsTable.value })
      .from(platformSettingsTable)
      .where(eq(platformSettingsTable.key, key));
    return parseHHmm(row?.value, fallback);
  } catch {
    return parseHHmm(undefined, fallback);
  }
}

