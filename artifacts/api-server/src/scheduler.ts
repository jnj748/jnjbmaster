interface RunReminderArgs {
  notificationType: "daily_journal_reminder_evening" | "daily_journal_reminder_morning";
  targetDate: string;       // 일보가 있어야 하는 날짜 (YYYY-MM-DD KST)
  buildTitle: () => string;
  buildMessage: (user: ReminderTargetUser) => string;
}

async function runDailyJournalReminderPass(args: RunReminderArgs): Promise<number> {
  const sinceUtc = startOfTodayKstAsUtc();
  const targets = await loadReminderTargets();
  if (targets.length === 0) return 0;

  // 1) 오늘(=now KST) 안에 같은 type 으로 이미 발송된 user 목록.
  const alreadySent = await db
    .select({ recipient: notificationsTable.recipientType })
    .from(notificationsTable)
    .where(
      and(
        eq(notificationsTable.notificationType, args.notificationType),
        gte(notificationsTable.createdAt, sinceUtc),
      ),
    );
  const sentUserIds = new Set<number>();
  for (const r of alreadySent) {
    const m = /^user:(\d+)$/.exec(r.recipient ?? "");
    if (m) sentUserIds.add(Number(m[1]));
  }

  // 2) targetDate 의 일보를 이미 가진 (building, role) 조합.
  const buildingIds = Array.from(new Set(targets.map((t) => t.buildingId).filter((b): b is number => typeof b === "number")));
  if (buildingIds.length === 0) return 0;
  const journals = await db
    .select({ buildingId: dailyJournalsTable.buildingId, role: dailyJournalsTable.role })
    .from(dailyJournalsTable)
    .where(
      and(
        inArray(dailyJournalsTable.buildingId, buildingIds),
        eq(dailyJournalsTable.journalDate, args.targetDate),
      ),
    );
  const haveJournal = new Set(journals.map((j) => `${j.buildingId}:${j.role}`));

  // 3) targetDate 출근 부재(결근/연차/출장) 사용자 → 발송 면제.
  const userIds = targets.map((t) => t.id);
  const attendanceRows = await db
    .select({ userId: attendanceTable.userId, status: attendanceTable.status })
    .from(attendanceTable)
    .where(
      and(
        inArray(attendanceTable.userId, userIds),
        eq(attendanceTable.checkDate, args.targetDate),
      ),
    );
  const exemptStatuses = new Set(["absent", "leave", "business_trip"]);
  const exemptUserIds = new Set<number>();
  for (const a of attendanceRows) {
    if (a.status && exemptStatuses.has(a.status)) exemptUserIds.add(a.userId);
  }

  let sent = 0;
  for (const u of targets) {
    if (sentUserIds.has(u.id)) continue;
    if (exemptUserIds.has(u.id)) continue;
    if (u.buildingId == null) continue;
    if (haveJournal.has(`${u.buildingId}:${u.role}`)) continue;

    await insertNotification({
      recipientType: `user:${u.id}`,
      notificationType: args.notificationType,
      title: args.buildTitle(),
      message: args.buildMessage(u),
      relatedEntityType: "daily_journal",
    });
    sent++;
  }
  return sent;
}

let lastEveningPassYmd: string | null = null;
let lastMorningPassYmd: string | null = null;

async function runDailyJournalReminderTick(): Promise<void> {
  const now = kstNow();
  const dow = now.getUTCDay(); // 0=일, 6=토 (KST 기준)
  // 사양: "평일에만 실행". 따라서 금요일 저녁 미작성자에게는 토요일 오전
  //   2차 리마인더가 가지 않는다 (의도된 동작 — 주말 알림 피로도 최소화).
  //   월요일 오전에도 일요일치 리마인더는 보내지 않는다 (대상이 토/일이라 평일 X).
  if (dow === 0 || dow === 6) return;

  const todayYmd = kstYmd(now);
  const yesterdayYmd = kstAddDays(todayYmd, -1);
  const nowMinutes = now.getUTCHours() * 60 + now.getUTCMinutes();

  const evening = await getReminderSetting(REMINDER_EVENING_KEY, REMINDER_DEFAULT_EVENING);
  const morning = await getReminderSetting(REMINDER_MORNING_KEY, REMINDER_DEFAULT_MORNING);

  // 저녁 패스: 오늘 KST 의 evening.h:evening.m 이후, 오늘 한 번만.
  if (lastEveningPassYmd !== todayYmd && nowMinutes >= evening.h * 60 + evening.m) {
    try {
      const n = await runDailyJournalReminderPass({
        notificationType: "daily_journal_reminder_evening",
        targetDate: todayYmd,
        buildTitle: () => "오늘 업무일지 한 번만 들여다볼까요?",
        buildMessage: () => `오늘(${todayYmd}) 업무일지가 아직 비어 있어요. 1분이면 충분합니다. 여기를 눌러 자동 작성을 시작해 보세요.`,
      });
      lastEveningPassYmd = todayYmd;
      if (n > 0) logger.info({ count: n, target: todayYmd }, "daily journal evening reminders sent");
    } catch (err) {
      logger.error({ err }, "daily journal evening reminder failed");
    }
  }

  // 오전 패스: 오늘 KST 의 morning.h:morning.m 이후, 오늘 한 번만. 어제 일보 대상.
  if (lastMorningPassYmd !== todayYmd && nowMinutes >= morning.h * 60 + morning.m) {
    try {
      const n = await runDailyJournalReminderPass({
        notificationType: "daily_journal_reminder_morning",
        targetDate: yesterdayYmd,
        buildTitle: () => "어제 업무일지를 마무리해 볼까요?",
        buildMessage: () => `어제(${yesterdayYmd}) 업무일지가 비어 있어요. 지난 날짜로 지금도 작성하실 수 있어요. 여기를 눌러 마무리해 주세요.`,
      });
      lastMorningPassYmd = todayYmd;
      if (n > 0) logger.info({ count: n, target: yesterdayYmd }, "daily journal morning reminders sent");
    } catch (err) {
      logger.error({ err }, "daily journal morning reminder failed");
    }
  }
}

// [Task #793] 분할부과 스케줄 월별 회차 인식.
//   active 상태인 expense_voucher_schedules 의 currentRound 를 현재 월에 맞춰 한 회차씩
//   진행시키며, 진행할 때마다 voucher_schedule.tick 을 발행한다. 회계엔진(T6) 구독자가
//   `(차) 5100 관리비용 / (대) 1200 선급비용` 분개를 자동 생성한다.
//
//   멱등성: currentRound 갱신은 CAS(WHERE current_round = oldVal) 로 처리. 동일 월에
//   여러 번 실행되어도 expectedRound 까지만 진행되므로 중복 분개가 발생하지 않는다.
//   마지막 회차의 monthlyAmount 는 totalAmount - monthlyAmount * (months-1) 로 보정해
//   선급비용 잔액이 정확히 0 이 되도록 한다.
export async function runVoucherScheduleMonthlyTick(now: Date = new Date()): Promise<{ advanced: number }> {
  const currentMonth = `${now.getUTCFullYear()}-${String(now.getUTCMonth() + 1).padStart(2, "0")}`;
  const actives = await db
    .select()
    .from(expenseVoucherSchedulesTable)
    .where(eq(expenseVoucherSchedulesTable.status, "active"));
  let advanced = 0;
  for (const s of actives) {
    const expected = expectedRound(s.startMonth, currentMonth, s.months);
    let cur = s.currentRound;
    while (cur < expected) {
      const next = cur + 1;
      const isFinal = next === s.months;
      const amount = computeRoundAmount(next, s.months, s.totalAmount, s.monthlyAmount);
      const newStatus = isFinal ? "completed" : "active";
      const [updated] = await db
        .update(expenseVoucherSchedulesTable)
        .set({ currentRound: next, status: newStatus })
        .where(and(
          eq(expenseVoucherSchedulesTable.id, s.id),
          eq(expenseVoucherSchedulesTable.currentRound, cur),
        ))
        .returning({ id: expenseVoucherSchedulesTable.id });
      if (!updated) break; // 다른 워커가 선점 — 다음 schedule 로
      const tickMonth = addMonths(s.startMonth, next - 1);
      publishScheduleTick({
        scheduleId: s.id,
        voucherId: s.voucherId,
        buildingId: s.buildingId ?? null,
        month: tickMonth,
        round: next,
        totalRounds: s.months,
        monthlyAmount: amount,
        reason: isFinal ? "completed" : "near_end",
      });
      advanced += 1;
      cur = next;
    }
  }
  if (advanced > 0) logger.info({ advanced, currentMonth }, "[Task #793] voucher schedule monthly ticks advanced");
  return { advanced };
}

// [Task #834] 오래 'requested' 상태로 멈춘 자동이체 의뢰 알림.
//   - status='requested' 인 행 중 (requestedAt ?? createdAt) 가 임계 시간(N시간)
//     이상 경과한 건을 buildingId 별로 묶어 매니저(`manager:<buildingId>`) 와
//     본사 운영자(`admin` fan-out) 에게 알림을 발송한다.
//   - 임계 시간은 환경변수 AUTO_DEBIT_STALE_THRESHOLD_HOURS 로 조절 (기본 24).
//   - 일 1회만 발송: 같은 (buildingId) 에 대해 오늘 이미 발송된
//     stale_auto_debit_requested 알림이 있으면 스킵.
const AUTO_DEBIT_STALE_THRESHOLD_HOURS = (() => {
  const raw = process.env.AUTO_DEBIT_STALE_THRESHOLD_HOURS;
  const n = raw ? Number(raw) : NaN;
  return Number.isFinite(n) && n >= 1 ? n : 24;
})();

async function runStaleAutoDebitRequestedAlert(): Promise<void> {
  const todayStr = getToday();
  const todayStart = new Date(todayStr);
  const cutoff = new Date(Date.now() - AUTO_DEBIT_STALE_THRESHOLD_HOURS * 60 * 60 * 1000);

  const stale = await db
    .select()
    .from(autoDebitResultsTable)
    .where(
      and(
        eq(autoDebitResultsTable.status, "requested"),
        sql`COALESCE(${autoDebitResultsTable.requestedAt}, ${autoDebitResultsTable.createdAt}) <= ${cutoff}`,
      ),
    );

  if (stale.length === 0) {
    logger.debug("[Task #834] no stale auto-debit requested rows");
    return;
  }

  // buildingId 별로 그룹핑
  const byBuilding = new Map<number, typeof stale>();
  for (const row of stale) {
    const arr = byBuilding.get(row.buildingId) ?? [];
    arr.push(row);
    byBuilding.set(row.buildingId, arr);
  }

  // 오늘 이미 발송된 (buildingId) 들은 스킵하기 위해 조회.
  const sentToday = await db
    .select()
    .from(notificationsTable)
    .where(
      and(
        eq(notificationsTable.notificationType, "stale_auto_debit_requested"),
        gte(notificationsTable.createdAt, todayStart),
      ),
    );
  const alreadySentBuildings = new Set<number>();
  for (const n of sentToday) {
    if (typeof n.relatedEntityId === "number") {
      alreadySentBuildings.add(n.relatedEntityId);
    }
  }

  let alertedBuildings = 0;
  let totalRows = 0;
  for (const [buildingId, rows] of byBuilding.entries()) {
    if (alreadySentBuildings.has(buildingId)) continue;
    const oldest = rows.reduce((acc, r) => {
      const ts = (r.requestedAt ?? r.createdAt).getTime();
      return ts < acc ? ts : acc;
    }, Date.now());
    const oldestHours = Math.floor((Date.now() - oldest) / (60 * 60 * 1000));
    const months = Array.from(new Set(rows.map((r) => r.billingMonth))).sort();
    const monthLabel = months.length === 1 ? months[0] : `${months[0]} 외 ${months.length - 1}건`;
    const sampleUnits = rows.slice(0, 5).map((r) => `${r.unitNumber}호`).join(", ");
    const suffix = rows.length > 5 ? ` 외 ${rows.length - 5}건` : "";
    const message = `자동이체 의뢰 ${rows.length}건이 ${AUTO_DEBIT_STALE_THRESHOLD_HOURS}시간 이상 'requested' 상태입니다 (가장 오래된 건: ${oldestHours}시간 경과, 부과월: ${monthLabel}, ${sampleUnits}${suffix}). PG 연동/webhook 점검이 필요합니다.`;

    // 매니저 알림 (manager:<buildingId> → 해당 건물 매니저 user 들; 없으면 admin 폴백)
    await insertNotification({
      recipientType: `manager:${buildingId}`,
      notificationType: "stale_auto_debit_requested",
      title: "자동이체 의뢰 장기 미응답",
      message,
      relatedEntityType: "auto_debit_results",
      relatedEntityId: buildingId,
    });

    // 본사 운영자(admin fan-out)에도 발송
    await insertNotification({
      recipientType: "admin",
      notificationType: "stale_auto_debit_requested",
      title: "자동이체 의뢰 장기 미응답",
      message: `[건물 #${buildingId}] ${message}`,
      relatedEntityType: "auto_debit_results",
      relatedEntityId: buildingId,
    });

    alertedBuildings += 1;
    totalRows += rows.length;
  }

  if (alertedBuildings > 0) {
    logger.info(
      { alertedBuildings, totalRows, thresholdHours: AUTO_DEBIT_STALE_THRESHOLD_HOURS },
      "[Task #834] stale auto-debit requested alerts sent",
    );
  }
}

let dailyTimer: ReturnType<typeof setInterval> | null = null;
let monthlyTimer: ReturnType<typeof setInterval> | null = null;
let reminderTimer: ReturnType<typeof setInterval> | null = null;
// [Task #781] 외부 발송 워커 타이머 — 1분 간격으로 due 잡 처리.
let dispatchTimer: ReturnType<typeof setInterval> | null = null;
// [Task #821] 자동이체 결과 폴링 워커 타이머 — 5분 간격으로 'requested' 행 갱신.
let autoDebitPollTimer: ReturnType<typeof setInterval> | null = null;

const AUTO_DEBIT_POLL_INTERVAL_MS = (() => {
  const raw = process.env.PG_AUTO_DEBIT_POLL_INTERVAL_MS;
  const n = raw ? Number(raw) : NaN;
  return Number.isFinite(n) && n >= 30_000 ? n : 5 * 60 * 1000;
})();

// [Task #833] 운영 가시성 — 실행 이력을 영속 저장하고, 일정 시간 동안 성공한
//   실행이 없으면 운영자에게 한 차례 경보를 보낸다.
const AUTO_DEBIT_STALE_THRESHOLD_MS = (() => {
  const raw = process.env.PG_AUTO_DEBIT_STALE_MS;
  const n = raw ? Number(raw) : NaN;
  return Number.isFinite(n) && n >= 60_000 ? n : 30 * 60 * 1000;
})();

async function maybeAlertStaleAutoDebitPoll(): Promise<void> {
  try {
    const [latest] = await db
      .select()
      .from(autoDebitPollRunsTable)
      .orderBy(desc(autoDebitPollRunsTable.startedAt))
      .limit(1);
    if (!latest) return;
    const cutoff = new Date(Date.now() - AUTO_DEBIT_STALE_THRESHOLD_MS);
    if (latest.startedAt > cutoff) return;

    const since = new Date(Date.now() - AUTO_DEBIT_STALE_THRESHOLD_MS);
    const existing = await db
      .select({ id: notificationsTable.id })
      .from(notificationsTable)
      .where(
        and(
          eq(notificationsTable.notificationType, "auto_debit_poll_stale"),
          gte(notificationsTable.createdAt, since),
        ),
      )
      .limit(1);
    if (existing.length > 0) return;

    const staleMinutes = Math.round(AUTO_DEBIT_STALE_THRESHOLD_MS / 60000);
    const messageBody = `최근 ${staleMinutes}분 동안 폴링 실행 이력이 없습니다 (마지막 실행: ${latest.startedAt.toISOString()}).`;

    await insertNotification({
      recipientType: "admin",
      notificationType: "auto_debit_poll_stale",
      title: "자동이체 폴링 잡 정지 의심",
      message: messageBody,
      relatedEntityType: "auto_debit_poll",
    });
    logger.warn({ lastStartedAt: latest.startedAt }, "[Task #833] auto-debit poll appears stale");

    // [Task #839] 모바일 알림 — 본사 담당자(platform_admin, hq_executive) 중
    //   전화번호가 등록된 사용자에게 카카오/SMS 발송을 큐잉한다.
    //   중복 방지: stale 임계 기간 이내에 이미 발송된 dispatch_jobs 가 있으면 skip.
    try {
      const { enqueueDispatch } = await import("./lib/external/adapter");
      const existingDispatch = await db
        .select({ id: dispatchJobsTable.id })
        .from(dispatchJobsTable)
        .where(
          and(
            eq(dispatchJobsTable.triggerSource, "auto_debit_poll_stale"),
            gte(dispatchJobsTable.createdAt, since),
          ),
        )
        .limit(1);
      if (existingDispatch.length === 0) {
        const hqAdmins = await db
          .select({ id: usersTable.id, phone: usersTable.phone, name: usersTable.name })
          .from(usersTable)
          .where(
            and(
              inArray(usersTable.role, ["platform_admin", "hq_executive"]),
              isNotNull(usersTable.phone),
            ),
          );
        for (const admin of hqAdmins) {
          if (!admin.phone) continue;
          await enqueueDispatch({
            buildingId: null,
            channel: "popbill_kakao",
            target: admin.phone,
            payload: {
              templateCode: "auto_debit_poll_stale",
              title: "자동이체 폴링 잡 정지 의심",
              message: messageBody,
              recipientName: admin.name,
            },
            triggerSource: "auto_debit_poll_stale",
            relatedEntityType: "auto_debit_poll",
            maxAttempts: 3,
          }).catch((dispatchErr) => {
            logger.warn({ err: dispatchErr, adminId: admin.id }, "[Task #839] kakao dispatch failed, trying SMS");
            return enqueueDispatch({
              buildingId: null,
              channel: "popbill_sms",
              target: admin.phone!,
              payload: {
                title: "자동이체 폴링 잡 정지 의심",
                message: messageBody,
                recipientName: admin.name,
              },
              triggerSource: "auto_debit_poll_stale",
              relatedEntityType: "auto_debit_poll",
              maxAttempts: 3,
            });
          });
        }
        if (hqAdmins.length > 0) {
          logger.info({ recipientCount: hqAdmins.length }, "[Task #839] auto-debit stale alert dispatched to mobile");
        }
      }
    } catch (dispatchErr) {
      logger.error({ err: dispatchErr }, "[Task #839] mobile dispatch for stale alert failed");
    }
  } catch (err) {
    logger.error({ err }, "[Task #833] stale auto-debit poll alert check failed");
  }
}

async function runAutoDebitPollTick(): Promise<void> {
  const startedAt = new Date();
  const startMs = Date.now();
  // [Task #833] Stale 평가는 "이번 tick 결과를 적재하기 전" 의 마지막 실행을
  //   기준으로 해야 한다. 적재 후에 평가하면 latest = 방금 넣은 현재 tick 이라
  //   항상 false 가 된다.
  await maybeAlertStaleAutoDebitPoll();
  // [Task #853] daily 틱 자체가 정지하는 경우 stale 알림이 영원히 안 나가는
  //   것을 막기 위해, 5분마다 도는 폴링 틱에서도 동일 검사를 수행한다.
  //   알림 자체는 24h dedupe 가 걸려 있어 중복 발송되지 않는다.
  maybeAlertStalePurgeJobs().catch((err) =>
    logger.error({ err }, "[Task #853] purge alert check failed in poll tick"),
  );
  let enabled = false;
  let scanned = 0;
  let updated = 0;
  let errMessage: string | null = null;
  try {
    const result = await pollAutoDebitPending();
    enabled = result.enabled;
    scanned = result.scanned;
    updated = result.updated;
    if (!enabled) {
      // PG_AUTO_DEBIT_POLL_URL 미설정 — 잡은 no-op.
    } else if (scanned > 0 || updated > 0) {
      logger.info({ scanned, updated }, "[Task #821] auto-debit poll tick completed");
    } else {
      logger.debug("[Task #821] auto-debit poll tick: no pending rows");
    }
  } catch (err) {
    errMessage = err instanceof Error ? err.message : String(err);
    logger.error({ err }, "[Task #821] auto-debit poll tick failed");
  } finally {
    // [Task #833] 매 tick 결과를 영속 저장 — 운영 화면에서 잡 가시성 확보.
    try {
      await db.insert(autoDebitPollRunsTable).values({
        startedAt,
        finishedAt: new Date(),
        durationMs: Date.now() - startMs,
        enabled,
        scanned,
        updated,
        error: errMessage,
      });
    } catch (persistErr) {
      logger.error({ err: persistErr }, "[Task #833] failed to persist auto-debit poll run");
    }
  }
}

export function startScheduler(): void {
  logger.info("Starting automated scheduler for privacy and vehicle tasks");

  runPrivacyDestructionAlerts().catch((err) => logger.error({ err }, "Scheduled privacy alert failed"));
  runPrivacyDestructionProcessing().catch((err) => logger.error({ err }, "Scheduled privacy destruction failed"));
  runVoucherScheduleMonthlyTick().catch((err) => logger.error({ err }, "[Task #793] voucher schedule monthly tick failed"));
  autoGenerateDraftsForUpcoming().catch((err) => logger.error({ err }, "Scheduled draft/RFQ automation failed"));
  runWeeklyReportAutoGeneration().catch((err) => logger.error({ err }, "Scheduled weekly report failed"));
  runDelinquencyDetection().catch((err) => logger.error({ err }, "Scheduled delinquency detection failed"));
  runDelinquencyAutoResolution().catch((err) => logger.error({ err }, "Scheduled delinquency auto-resolution failed"));
  runNoticePostingSuggestion().catch((err) => logger.error({ err }, "Scheduled notice posting suggestion failed"));
  runStaleAutoDebitRequestedAlert().catch((err) => logger.error({ err }, "[Task #834] stale auto-debit alert failed"));
  // [Task #226] 미열람 견적 일부 환불 (기본 7일 이상 미열람 시 60% 환불).
  refundUnviewedQuotes()
    .then((r) => { if (r.refundedCount > 0) logger.info(r, "Unviewed-quote refund job processed"); })
    .catch((err) => logger.error({ err }, "Unviewed-quote refund job failed"));

  const now = new Date();
  if (now.getDate() === 1) {
    runVehicleMonthlyInspection().catch((err) => logger.error({ err }, "Scheduled vehicle inspection failed"));
  }
  if (now.getDay() === 1 && now.getDate() <= 7) {
    runMonthlyReportAutoGeneration().catch((err) => logger.error({ err }, "Scheduled monthly report failed"));
  }

  dailyTimer = setInterval(async () => {
    logger.info("Running daily scheduled tasks");
    try {
      await runPrivacyDestructionAlerts();
      await runPrivacyDestructionProcessing();
      await runVoucherScheduleMonthlyTick();
      await autoGenerateDraftsForUpcoming();
      await runWeeklyReportAutoGeneration();
      await runMonthlyReportAutoGeneration();
      await runDelinquencyDetection();
      await runDelinquencyAutoResolution();
      await runNoticePostingSuggestion();
      await runStaleAutoDebitRequestedAlert();
      await refundUnviewedQuotes();
      // [Task #296] usage_events 보존 정책: 180일 이전 이벤트 자동 정리.
      const purged = await purgeOldUsageEvents(180);
      if (purged > 0) logger.info({ purged }, "Purged old usage_events rows");
      // [Task #840] auto_debit_poll_runs 보존 정책: 기본 90일 이전 이력 자동 정리.
      const purgedPollRuns = await purgeOldAutoDebitPollRuns();
      if (purgedPollRuns > 0) logger.info({ purged: purgedPollRuns }, "Purged old auto_debit_poll_runs rows");
      // [Task #853] audit 테이블 자체 보존 정책: 기본 365일 이전 row 자동 정리.
      //   결과는 같은 audit 테이블에 jobName='operational_purge_runs' 로 기록되어
      //   다른 잡과 동일한 방식으로 모니터링 된다.
      const purgedAudit = await purgeOldOperationalPurgeRuns();
      if (purgedAudit > 0) logger.info({ purged: purgedAudit }, "Purged old operational_purge_runs rows");
      // [Task #853] purge 잡 자체에 대한 stale/error 알림.
      await maybeAlertStalePurgeJobs();
    } catch (err) {
      logger.error({ err }, "Daily scheduled task failed");
    }
  }, 24 * 60 * 60 * 1000);

  monthlyTimer = setInterval(async () => {
    const currentDate = new Date();
    if (currentDate.getDate() === 1) {
      logger.info("Running monthly vehicle inspection (1st of month)");
      try {
        await runVehicleMonthlyInspection();
      } catch (err) {
        logger.error({ err }, "Monthly vehicle inspection failed");
      }
    }
  }, 24 * 60 * 60 * 1000);

  // [Task #609] 일보 작성 독려: 15분 간격으로 평가하며, 설정된 발송 시각을
  //   넘겼고 오늘 아직 발송 안 했으면 한 번 발송. 평일/본인 토글/결근/중복 모두 내부에서 체크.
  runDailyJournalReminderTick().catch((err) => logger.error({ err }, "Daily journal reminder tick failed"));
  reminderTimer = setInterval(() => {
    runDailyJournalReminderTick().catch((err) => logger.error({ err }, "Daily journal reminder tick failed"));
  }, 15 * 60 * 1000);

  // [Task #781] 외부 발송 잡 큐 워커 — 1분 간격. due(scheduledAt<=now) 인 queued/failed 잡을 처리.
  dispatchTimer = setInterval(() => {
    processDueJobs(50).catch((err) => logger.error({ err }, "Dispatch worker tick failed"));
  }, 60 * 1000);

  // [Task #821] 자동이체 결과 폴링 워커 — 기본 5분 간격. PG_AUTO_DEBIT_POLL_URL 미설정 시 no-op.
  //   부팅 직후 1회 실행 후 주기적 실행. PG_AUTO_DEBIT_POLL_INTERVAL_MS (>=30000) 로 주기 조절 가능.
  runAutoDebitPollTick();
  autoDebitPollTimer = setInterval(() => {
    runAutoDebitPollTick();
  }, AUTO_DEBIT_POLL_INTERVAL_MS);
  logger.info({ intervalMs: AUTO_DEBIT_POLL_INTERVAL_MS }, "[Task #821] auto-debit poll worker scheduled");
}

export function stopScheduler(): void {
  if (dailyTimer) {
    clearInterval(dailyTimer);
    dailyTimer = null;
  }
  if (monthlyTimer) {
    clearInterval(monthlyTimer);
    monthlyTimer = null;
  }
  if (reminderTimer) {
    clearInterval(reminderTimer);
    reminderTimer = null;
  }
  if (dispatchTimer) {
    clearInterval(dispatchTimer);
    dispatchTimer = null;
  }
  if (autoDebitPollTimer) {
    clearInterval(autoDebitPollTimer);
    autoDebitPollTimer = null;
  }
  logger.info("Scheduler stopped");
}
