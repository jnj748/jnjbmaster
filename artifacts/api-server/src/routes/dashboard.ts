router.get("/reports/weekly", async (req, res): Promise<void> => {
  const weekStart = req.query.weekStart as string | undefined;
  if (!weekStart || !/^\d{4}-\d{2}-\d{2}$/.test(weekStart)) {
    res.status(400).json({ error: "weekStart is required (YYYY-MM-DD)" });
    return;
  }
  const weekEnd = new Date(new Date(weekStart).getTime() + 6 * 24 * 60 * 60 * 1000)
    .toISOString()
    .split("T")[0];

  const nextWeekStart = new Date(new Date(weekStart).getTime() + 7 * 24 * 60 * 60 * 1000)
    .toISOString()
    .split("T")[0];
  const nextWeekEnd = new Date(new Date(weekStart).getTime() + 13 * 24 * 60 * 60 * 1000)
    .toISOString()
    .split("T")[0];

  const allTasks = await db.select().from(tasksTable);
  const weekTasks = allTasks.filter(
    (t) => t.dueDate && t.dueDate >= weekStart && t.dueDate <= weekEnd
  );

  const completedTasks = weekTasks.filter((t) => t.status === "completed");
  const pendingTasks = weekTasks.filter((t) => t.status !== "completed");

  const inspectionsDue = await db
    .select()
    .from(inspectionsTable)
    .where(
      and(
        gte(inspectionsTable.nextDueDate, weekStart),
        lte(inspectionsTable.nextDueDate, weekEnd)
      )
    );

  const nextWeekInspectionRows = await db
    .select()
    .from(inspectionsTable)
    .where(
      and(
        gte(inspectionsTable.nextDueDate, nextWeekStart),
        lte(inspectionsTable.nextDueDate, nextWeekEnd)
      )
    );

  const nextWeekInspections = nextWeekInspectionRows.map((i) => ({
    id: i.id,
    name: i.name,
    category: i.category,
    nextDueDate: i.nextDueDate,
  }));

  const taxDue = await db
    .select()
    .from(taxSchedulesTable)
    .where(
      and(
        gte(taxSchedulesTable.dueDate, weekStart),
        lte(taxSchedulesTable.dueDate, weekEnd)
      )
    );

  const categoryMap: Record<string, number> = {};
  for (const task of weekTasks) {
    categoryMap[task.category] = (categoryMap[task.category] || 0) + 1;
  }

  const tasksByCategory = Object.entries(categoryMap).map(([category, count]) => ({
    category,
    count,
  }));

  const highlights: string[] = [];
  if (completedTasks.length > 0) {
    highlights.push(`${completedTasks.length}건의 업무가 완료되었습니다.`);
  }
  if (inspectionsDue.length > 0) {
    highlights.push(`${inspectionsDue.length}건의 법정 점검이 예정되어 있습니다.`);
  }
  if (taxDue.length > 0) {
    highlights.push(`${taxDue.length}건의 세무 일정이 마감됩니다.`);
  }
  if (nextWeekInspections.length > 0) {
    highlights.push(`다음 주 ${nextWeekInspections.length}건의 법정 점검이 예정되어 있습니다: ${nextWeekInspections.map((i) => i.name).join(", ")}`);
  }

  const report = {
    weekStart,
    weekEnd,
    totalTasks: weekTasks.length,
    completedTasks: completedTasks.length,
    pendingTasks: pendingTasks.length,
    inspectionsDue: inspectionsDue.length,
    taxSchedulesDue: taxDue.length,
    tasksByCategory,
    highlights,
    nextWeekInspections,
  };

  res.json(GetWeeklyReportResponse.parse(report));
});

router.get("/dashboard/analytics", async (_req, res): Promise<void> => {
  const today = new Date();
  const todayStr = today.toISOString().split("T")[0];

  const allUnits = await db.select().from(unitsTable);
  const occupiedUnits = allUnits.filter((u) => u.status === "occupied");
  const unpaidCount = Math.floor(occupiedUnits.length * 0.15);
  const totalUnpaid = unpaidCount * 250000;
  const unpaidByMonth: Array<{ month: string; amount: number; count: number }> = [];
  for (let i = 2; i >= 0; i--) {
    const d = new Date(today.getFullYear(), today.getMonth() - i, 1);
    const m = `${d.getFullYear()}-${String(d.getMonth() + 1).padStart(2, "0")}`;
    const cnt = Math.max(0, unpaidCount - i * 2);
    unpaidByMonth.push({ month: m, amount: cnt * 250000, count: cnt });
  }

  const unpaidByCategory = [
    { category: "관리비", amount: Math.round(totalUnpaid * 0.6) },
    { category: "수선유지비", amount: Math.round(totalUnpaid * 0.2) },
    { category: "장기수선충당금", amount: Math.round(totalUnpaid * 0.15) },
    { category: "기타", amount: Math.round(totalUnpaid * 0.05) },
  ];

  const dayOfWeek = today.getDay();
  const mondayOffset = dayOfWeek === 0 ? -6 : 1 - dayOfWeek;
  const monday = new Date(today);
  monday.setDate(today.getDate() + mondayOffset);
  const mondayStr = monday.toISOString().split("T")[0];
  const sundayDate = new Date(monday);
  sundayDate.setDate(monday.getDate() + 6);
  const sundayStr = sundayDate.toISOString().split("T")[0];

  const attendanceRows = await db
    .select()
    .from(attendanceTable)
    .where(
      and(
        gte(attendanceTable.checkDate, mondayStr),
        lte(attendanceTable.checkDate, sundayStr)
      )
    );

  const dayLabels = ["월", "화", "수", "목", "금", "토", "일"];
  const workHoursByDay: Array<{ day: string; date: string; hours: number; staffCount: number }> = [];

  for (let d = 0; d < 7; d++) {
    const dateObj = new Date(monday);
    dateObj.setDate(monday.getDate() + d);
    const dateStr = dateObj.toISOString().split("T")[0];

    const dayRecords = attendanceRows.filter((r) => r.checkDate === dateStr);
    const userIds = [...new Set(dayRecords.map((r) => r.userId))];

    let totalHours = 0;
    for (const uid of userIds) {
      const checkIn = dayRecords.find((r) => r.userId === uid && r.checkType === "check_in");
      const checkOut = dayRecords.find((r) => r.userId === uid && r.checkType === "check_out");
      if (checkIn?.checkInTime && checkOut?.checkOutTime) {
        const diff = (new Date(checkOut.checkOutTime).getTime() - new Date(checkIn.checkInTime).getTime()) / (1000 * 60 * 60);
        totalHours += Math.min(diff, 12);
      } else if (checkIn?.checkInTime) {
        totalHours += 8;
      }
    }

    workHoursByDay.push({
      day: dayLabels[d],
      date: dateStr,
      hours: Math.round(totalHours * 10) / 10,
      staffCount: userIds.length,
    });
  }

  const destructionTenants = await db
    .select()
    .from(tenantsTable)
    .where(
      and(
        isNotNull(tenantsTable.dataDestructionDate),
        lte(tenantsTable.dataDestructionDate, todayStr),
        eq(tenantsTable.status, "moved_out")
      )
    );

  const destructionOwners = await db
    .select()
    .from(ownersTable)
    .where(
      and(
        isNotNull(ownersTable.dataDestructionDate),
        lte(ownersTable.dataDestructionDate, todayStr),
        eq(ownersTable.status, "moved_out")
      )
    );

  const userRole = _req.user?.role;
  const canSeePii = ["manager", "platform_admin"].includes(userRole ?? "");

  const destructionTargets = [
    ...destructionTenants.map((t) => ({
      id: t.id,
      type: "tenant" as const,
      name: canSeePii ? t.tenantName : "***",
      unit: t.unit,
      destructionDate: t.dataDestructionDate!,
      moveOutDate: canSeePii ? t.moveOutDate : null,
    })),
    ...destructionOwners.map((o) => ({
      id: o.id,
      type: "owner" as const,
      name: canSeePii ? o.ownerName : "***",
      unit: o.unit,
      destructionDate: o.dataDestructionDate!,
      moveOutDate: canSeePii ? o.moveOutDate : null,
    })),
  ];

  res.json({
    unpaidSummary: {
      totalUnpaid,
      unpaidCount,
      totalUnits: occupiedUnits.length,
      unpaidRate: occupiedUnits.length > 0 ? Math.round((unpaidCount / occupiedUnits.length) * 100) : 0,
    },
    unpaidByMonth,
    unpaidByCategory,
    workHoursByDay,
    dataDestructionTargets: destructionTargets,
    dataDestructionCount: destructionTargets.length,
  });
});

